const getProduct = (id) => new Promise(
    (resolve, reject) => {
        setTimeout(() => {
            resolve(
                ProductList.find(
                    (item) => {
                        return item.id === parseInt(id);
                    })
            )
        }, 500);
    });

const getProductListByCategory = (id) => new Promise(
    (resolve, reject) => {
        setTimeout(() => {
            resolve(
                ProductList.filter(
                    (item) => {
                        return item.category === parseInt(id);
                    })
            )
        }, 500);
    });

